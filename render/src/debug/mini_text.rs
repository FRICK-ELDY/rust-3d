// 5x7 ドットフォント（1px 間隔を空けて 6x8 ピッチ）で ASCII の一部だけ。
// 必要最低限: 数字 / アルファベット大文字 / 記号 . : x % 空白
pub struct MiniText {
    buf: Vec<u8>,        // RGBA
    pub tex_w: u32,
    pub tex_h: u32,
    pitch_x: u32,
    pitch_y: u32,
}
impl MiniText {
    pub fn new(width: u32, height: u32) -> Self {
        Self {
            buf: vec![0u8; (width*height*4) as usize],
            tex_w: width,
            tex_h: height,
            pitch_x: 6,
            pitch_y: 8,
        }
    }
    pub fn clear(&mut self){ self.buf.fill(0); }
    pub fn as_rgba(&self) -> &[u8]{ &self.buf }

    pub fn draw_text(&mut self, x: u32, y: u32, s: &str, rgba: [u8;4]) {
        let start_x = x;
        let mut cx = x;
        let mut cy = y;

        for ch in s.chars() {
            match ch {
                '\n' => {
                    // 次の行へ
                    cx = start_x;
                    cy += self.pitch_y;
                    if cy + 7 >= self.tex_h { break; } // 下端で打ち切り
                }
                _ => {
                    // 右端は切らずに折り返す（任意：不要なら if を削って描かないでもOK）
                    if cx + 5 >= self.tex_w {
                        cx = start_x;
                        cy += self.pitch_y;
                        if cy + 7 >= self.tex_h { break; }
                    }
                    self.draw_char(cx, cy, ch, rgba);
                    cx += self.pitch_x;
                }
            }
        }
    }
    fn draw_char(&mut self, x: u32, y: u32, ch: char, rgba: [u8;4]) {
        let pat = glyph(ch);
        for (row_i, row) in pat.iter().enumerate() {
            for col in 0..5 {
                if (row >> (4 - col)) & 1 == 1 {
                    self.put_pixel(x + col as u32, y + row_i as u32, rgba);
                }
            }
        }
    }
    fn put_pixel(&mut self, x: u32, y: u32, rgba: [u8;4]) {
        if x >= self.tex_w || y >= self.tex_h { return; }
        let i = ((y*self.tex_w + x)*4) as usize;
        self.buf[i..i+4].copy_from_slice(&rgba);
    }
    pub fn resize(&mut self, width: u32, height: u32) {
        if width == self.tex_w && height == self.tex_h { return; }
        self.tex_w = width;
        self.tex_h = height;
        self.buf = vec![0u8; (width * height * 4) as usize];
    }
}

